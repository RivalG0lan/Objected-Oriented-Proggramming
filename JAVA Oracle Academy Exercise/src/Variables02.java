public class Variables02 {
    public static void ModVar() {
        String x = "puppy";
        String y = x;
        System.out.println(x);
        System.out.println(y);
        
        x = "kitty";
        System.out.println(x);
        System.out.println(y);
        
        y = "bunny";
        System.out.println(x);
        System.out.println(y);
       
    }
}
